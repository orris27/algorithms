class Solution {
public:
    int climbStairs(int n) {
        if (n == 0)
            return 0;
        if (n == 1)
            return 1;
        if (n == 2)
            return 2;
        // 0, 1, 2, 3, 5, 8
        int p1 = 1, p2 = 2, cur = 2;
        for(int i =3;i<=n;++i)
        {
            cur = p1 + p2;
            p1 = p2;
            p2 = cur;
        }

        return cur;

    }
};



