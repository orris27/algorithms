class Solution {
public:
    int maxProfit(vector<int>& prices) {
        int n=prices.size();
        if (n == 0 || n == 1 || (n == 2 && prices[1] < prices[0]))
            return 0;
        int buy=prices[0], res=0;

        for(int i=1;i<n;++i)
        {
            res = max(prices[i] - buy, res); // update buying price
            if (buy > prices[i])
                buy = prices[i];
        }
        return res;
        
    }
};
